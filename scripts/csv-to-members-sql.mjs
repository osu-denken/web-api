// 名簿スプレッドシートを CSV エクスポートしたものを D1 の INSERT 文に変換する。
//
//   node scripts/csv-to-members-sql.mjs members.csv > migrations/0003_seed_members.sql
//   npx wrangler d1 migrations apply osu-denken-members --remote
//
// CSV の1行目はヘッダーで、num / name / furigana / role / joinDate / leaveDate / tel /
// email / birthday / customData 列を見る。
//
// 生成した SQL には電話番号が平文で入る。リポジトリにコミットしないこと。

import { readFileSync } from "node:fs";

const ALLOWED_EMAIL_DOMAIN = "ge.osaka-sandai.ac.jp";

// util/permission.ts の Role と一致させること
const ROLE = {
    Member: 1 << 1,
    Other: 1 << 2,
    Executive: 1 << 3,
    Manager: 1 << 4,
    Accountant: 1 << 5,
    ChiefClerk: 1 << 6,
    ViceLeader: 1 << 7,
    Leader: 1 << 8,
};

const ROLE_NAMES = {
    "部員": ROLE.Member,
    "その他": ROLE.Other,
    "幹部": ROLE.Executive,
    "マネージャー": ROLE.Manager,
    "会計": ROLE.Accountant,
    "主務": ROLE.ChiefClerk,
    "副部長": ROLE.ViceLeader,
    "部長": ROLE.Leader,
};

// role 列に紛れ込む在籍状態。役職ビットではなく status に写す
const STATUS_NAMES = {
    "卒業": "graduated",
    "退部": "withdrawn",
};

const EXECUTIVE_ROLES =
    ROLE.Manager | ROLE.Accountant | ROLE.ChiefClerk | ROLE.ViceLeader | ROLE.Leader;

/** 引用符とエスケープされた引用符を解釈する RFC4180 風のパーサ */
function parseCsv(text) {
    const rows = [];
    let row = [], field = "", quoted = false;

    for (let i = 0; i < text.length; i++) {
        const c = text[i];

        if (quoted) {
            if (c === '"' && text[i + 1] === '"') { field += '"'; i++; }
            else if (c === '"') quoted = false;
            else field += c;
            continue;
        }

        if (c === '"') quoted = true;
        else if (c === ",") { row.push(field); field = ""; }
        else if (c === "\n") { row.push(field); rows.push(row); row = []; field = ""; }
        else if (c !== "\r") field += c;
    }

    if (field || row.length) { row.push(field); rows.push(row); }

    return rows.filter(r => r.some(cell => cell !== ""));
}

const normalizeStudentId = (num) => {
    const withoutPrefix = num.startsWith("s") || num.startsWith("S") ? num.slice(1) : num;
    return withoutPrefix.trim().toUpperCase();
};

/** 役職セルは "部長" や "部員/会計" のような表記を想定する */
const toRoleBits = (roleCell) => {
    let bits = 0;
    for (const name of roleCell.split(/[\/,、]/).map(s => s.trim())) {
        bits |= ROLE_NAMES[name] ?? 0;
    }

    // 幹部を兼ねる役職には Executive を補う (normalizeRoles と同じ規則)
    if (bits & EXECUTIVE_ROLES) bits |= ROLE.Executive;
    if (bits === 0) bits = ROLE.Member;

    return bits;
};

/** role 列に在籍状態が書かれていればそれを、なければ在籍中とみなす */
const toStatus = (roleCell, leaveDate) => {
    for (const name of roleCell.split(/[\/,、]/).map(s => s.trim())) {
        if (STATUS_NAMES[name]) return STATUS_NAMES[name];
    }

    return leaveDate ? "withdrawn" : "active";
};

const sqlString = (value) =>
    value === null || value === undefined || value === "" ? "NULL" : `'${String(value).replace(/'/g, "''")}'`;

/** "2022/05/26" のような表記を ISO8601 に揃える。input[type=date] が読めるようにするため */
const toIsoDate = (value) => value.replace(/\//g, "-");

const path = process.argv[2];
if (!path) {
    console.error("usage: node scripts/csv-to-members-sql.mjs <members.csv>");
    process.exit(1);
}

const [headers, ...rows] = parseCsv(readFileSync(path, "utf8"));
const col = (row, key) => (row[headers.indexOf(key)] ?? "").trim();

console.log("-- generated by scripts/csv-to-members-sql.mjs");

for (const row of rows) {
    const num = col(row, "num");
    if (!num) continue;

    const studentId = normalizeStudentId(num);

    // email 列があればそれが正。無い行だけ学籍番号から組み立てる
    const email = col(row, "email") || `s${studentId.toLowerCase()}@${ALLOWED_EMAIL_DOMAIN}`;

    const roleCell = col(row, "role");
    const leaveDate = col(row, "leaveDate");

    // スキーマに列を持たない項目は custom_data の JSON に逃がす
    const customData = JSON.parse(col(row, "customData") || "{}");
    // if (col(row, "birthday")) customData.birthday = col(row, "birthday");

    console.log(
        `INSERT INTO members (student_id, email, name, furigana, tel, status, role_bits, join_date, leave_date, custom_data) VALUES (` +
        [
            sqlString(studentId),
            sqlString(email.toLowerCase()),
            sqlString(col(row, "name")),
            sqlString(col(row, "furigana")),
            sqlString(col(row, "tel")),
            sqlString(toStatus(roleCell, leaveDate)),
            toRoleBits(roleCell),
            sqlString(toIsoDate(col(row, "joinDate"))),
            sqlString(toIsoDate(leaveDate)),
            sqlString(JSON.stringify(customData)),
        ].join(", ") +
        `)\n` +
        // 0002_seed_leader.sql が入れた行と衝突しても上書きで通す
        `ON CONFLICT(student_id) DO UPDATE SET\n` +
        `    email = excluded.email, name = excluded.name, furigana = excluded.furigana,\n` +
        `    tel = excluded.tel, status = excluded.status, role_bits = excluded.role_bits,\n` +
        `    join_date = excluded.join_date, leave_date = excluded.leave_date,\n` +
        `    custom_data = excluded.custom_data;`
    );
}
