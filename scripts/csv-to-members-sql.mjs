// 名簿スプレッドシートを CSV エクスポートしたものを D1 の INSERT 文に変換する。
//
//   node scripts/csv-to-members-sql.mjs members.csv > migrations/0002_seed_members.sql
//   npx wrangler d1 migrations apply osu-denken-members --remote
//
// CSV の1行目はヘッダーで、num / name / furigana / role / joinDate / permit 列を見る。

import { readFileSync } from "node:fs";

const ALLOWED_EMAIL_DOMAIN = "ge.osaka-sandai.ac.jp";

// util/permission.ts の Role と一致させること
const ROLE = {
    Member: 1 << 1,
    Other: 1 << 2,
    Executive: 1 << 3,
    Manager: 1 << 4,
    Accountant: 1 << 5,
    ChiefClerk: 1 << 6,
    ViceLeader: 1 << 7,
    Leader: 1 << 8,
};

const ROLE_NAMES = {
    "部員": ROLE.Member,
    "その他": ROLE.Other,
    "幹部": ROLE.Executive,
    "マネージャー": ROLE.Manager,
    "会計": ROLE.Accountant,
    "主務": ROLE.ChiefClerk,
    "副部長": ROLE.ViceLeader,
    "部長": ROLE.Leader,
};

const EXECUTIVE_ROLES =
    ROLE.Manager | ROLE.Accountant | ROLE.ChiefClerk | ROLE.ViceLeader | ROLE.Leader;

/** 引用符・カンマ・改行を含まない前提の素朴な CSV パーサ */
const parseCsv = (text) =>
    text.trim().split(/\r?\n/).map(line => line.split(",").map(cell => cell.trim()));

const normalizeStudentId = (num) => {
    const withoutPrefix = num.startsWith("s") || num.startsWith("S") ? num.slice(1) : num;
    return withoutPrefix.toUpperCase();
};

/** 役職セルは "部長" や "部員/会計" のような表記を想定する */
const toRoleBits = (roleCell) => {
    let bits = 0;
    for (const name of roleCell.split(/[\/,、]/).map(s => s.trim())) {
        bits |= ROLE_NAMES[name] ?? 0;
    }

    // 幹部を兼ねる役職には Executive を補う (normalizeRoles と同じ規則)
    if (bits & EXECUTIVE_ROLES) bits |= ROLE.Executive;
    if (bits === 0) bits = ROLE.Member;

    return bits;
};

const sqlString = (value) =>
    value === null || value === undefined || value === "" ? "NULL" : `'${String(value).replace(/'/g, "''")}'`;

const path = process.argv[2];
if (!path) {
    console.error("usage: node scripts/csv-to-members-sql.mjs <members.csv>");
    process.exit(1);
}

const [headers, ...rows] = parseCsv(readFileSync(path, "utf8"));
const col = (row, key) => row[headers.indexOf(key)] ?? "";

console.log("-- generated by scripts/csv-to-members-sql.mjs");

for (const row of rows) {
    const num = col(row, "num");
    if (!num) continue;

    const studentId = normalizeStudentId(num);
    const email = `s${studentId.toLowerCase()}@${ALLOWED_EMAIL_DOMAIN}`;

    // permit=1 だった人だけを在籍中として引き継ぐ。それ以外は退部扱いにして権限を持たせない
    const status = col(row, "permit") === "1" ? "active" : "withdrawn";

    console.log(
        `INSERT INTO members (student_id, email, name, furigana, status, role_bits, join_date) VALUES (` +
        [
            sqlString(studentId),
            sqlString(email),
            sqlString(col(row, "name")),
            sqlString(col(row, "furigana")),
            sqlString(status),
            toRoleBits(col(row, "role")),
            sqlString(col(row, "joinDate")),
        ].join(", ") +
        `);`
    );
}
